public class Multply{
 
    public init(){}
    
    public func squaredMultiply(_ a: Int, _ b: Int) -> Int{
        return (a * a) * (b * b)
    }
    
    public func simpleMultiply(_ a: Int, _ b: Int) -> Int{
        return a * b
    }
    
    public func squaredMultiply(_ a: Double, _ b: Double) -> Double{
        return (a * a) * (b * b)
    }
    
    public func simpleMultiply(_ a: Double, _ b: Double) -> Double{
        return a * b
    }
    
}
